package com.mgc.common.model;

public class JscInfos {
	private String saveUrl;
	private int createNum;
	private String phoneNumber;
	private String tencentNumber;
	private String filePrex;
	public String getSaveUrl() {
		return saveUrl;
	}
	public void setSaveUrl(String saveUrl) {
		this.saveUrl = saveUrl;
	}
	
	public int getCreateNum() {
		return createNum;
	}
	public void setCreateNum(int createNum) {
		this.createNum = createNum;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getTencentNumber() {
		return tencentNumber;
	}
	public void setTencentNumber(String tencentNumber) {
		this.tencentNumber = tencentNumber;
	}
	public String getFilePrex() {
		return filePrex;
	}
	public void setFilePrex(String filePrex) {
		this.filePrex = filePrex;
	}
	
}
